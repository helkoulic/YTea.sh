Loading() { for I in / - \\ ""
do
echo -en "\r$I"
sleep .2
done
}

Again(){
read -ep "Search--> " search


for S in $(echo "$search" | sed 's/ /+/g; s/,/ /g')
do	
	if [[ $search == "--exit-now" ]];then
	echo "Salamm!!!"
	sleep .2
		exit
	fi

	
Loading && xdg-open https://www.youtube.com/search?q="$S" 

	done
}

while true 
do
	Again
done



elif [[ $1 == "-help" || $1 == "-h" || $1 == "--help" ]];then
	echo -e "- To perform multiple searches simultaneously, separate your queries with \e[0;1mcommas (,)\e[0m. 
	eg: bash YTea.sh what is metadata, how to remove metadata from images, python socket explained

Arguments:
	--help =======> to display this page
	--exit-now ===> to exit"



elif [[ $* != "" ]];then
	
	for A in $(echo "$*" | sed 's/ /+/g; s/,/ /g')

	do
		xdg-open https://www.youtube.com/search?q=$A
		exit
	done




else
	echo "ERROR"
fi




